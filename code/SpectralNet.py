# TRAINING LOOP

def train_one_epoch(encoder, capsnet, loader, optimizer, beta):
    encoder.train()
    capsnet.train()
    total    = 0.0
    comp_sum = {'L_G': 0, 'L_S': 0, 'L_SD': 0, 'L_C': 0}

    for patches, abuns_gt, labels in loader:
        patches       = patches.to(DEVICE, non_blocking=True)
        labels        = labels.to(DEVICE,  non_blocking=True)
        labels_onehot = torch.eye(NUM_CLASSES, device=DEVICE)[labels]
        x_center      = get_center_spectrum(patches)

        mu, log_var         = encoder(patches)
        z                   = reparameterize(mu, log_var)
        abundances, x_recon = capsnet(z, labels_onehot)

        loss, comps = total_loss(
            mu, log_var, x_center, x_recon, abundances, labels_onehot, beta)

        optimizer.zero_grad()
        loss.backward()
        torch.nn.utils.clip_grad_norm_(all_params, max_norm=1.0)
        optimizer.step()

        total += loss.item()
        for k in comp_sum:
            comp_sum[k] += comps[k]

    n = len(loader)
    return total / n, {k: v / n for k, v in comp_sum.items()}


@torch.no_grad()
def evaluate(encoder, capsnet, loader, per_class=False):
    encoder.eval()
    capsnet.eval()
    correct   = 0
    total     = 0
    nrmse_sum = 0.0
    class_correct = np.zeros(NUM_CLASSES)
    class_total   = np.zeros(NUM_CLASSES)

    confusion = np.zeros((NUM_CLASSES, NUM_CLASSES), dtype=np.int64)

    for patches, abuns_gt, labels in loader:
        patches  = patches.to(DEVICE,  non_blocking=True)
        abuns_gt = abuns_gt.to(DEVICE, non_blocking=True)
        labels   = labels.to(DEVICE,   non_blocking=True)

        mu, log_var   = encoder(patches)
        z             = reparameterize(mu, log_var)
        abundances, _ = capsnet(z)

        preds = abundances.argmax(dim=1)
        correct   += (preds == labels).sum().item()
        total     += labels.size(0)
        nrmse_sum += torch.sqrt(
            ((abundances - abuns_gt)**2).mean(dim=1)).sum().item()

        # Fill confusion matrix
        preds_np  = preds.cpu().numpy()
        labels_np = labels.cpu().numpy()
        for p, t in zip(preds_np, labels_np):
            confusion[t, p] += 1

        if per_class:
            for cls in range(NUM_CLASSES):
                mask = (labels == cls)
                class_total[cls]   += mask.sum().item()
                class_correct[cls] += (preds[mask] == cls).sum().item()

    acc  = 100 * correct / total
    nrmse = nrmse_sum / total

    # Kappa calculation
    n     = confusion.sum()
    # observed agreement
    po    = np.diag(confusion).sum() / n
    pe    = (confusion.sum(axis=1) * confusion.sum(axis=0)).sum() / (n ** 2)
    kappa = (po - pe) / (1 - pe + 1e-10)


    if per_class:
        return acc, nrmse, kappa, class_correct, class_total
    return acc, nrmse, kappa


